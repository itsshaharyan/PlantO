    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        // delete category
        int result=JOptionPane.showConfirmDialog(null, "Do you really wanna delete", "Confirmation Dialog", JOptionPane.YES_NO_OPTION);
        if(result==JOptionPane.YES_OPTION){
            try{
                int ri=rTable.getSelectedRow();
                if(ri!=-1){
                    String n=(String)rTable.getValueAt(ri,1);
                    int c=db.DbConnect.st.executeUpdate("delete from category where category_name='"+n+"'");
                    if(c!=0){
                        JOptionPane.showMessageDialog(null, "Deleted Successfully!");
                        displayTableData();
                    }else{
                        JOptionPane.showMessageDialog(null, "Deletion Failed!");
                    }
                }else{
                    JOptionPane.showMessageDialog(null, "plz select the row to delete!");
                }
            }catch(Exception ex){
                JOptionPane.showMessageDialog(null, ex);
            }
        }
    }//GEN-LAST:event_jButton2ActionPerformed

