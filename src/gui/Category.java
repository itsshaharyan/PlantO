    private void displayTableData(){
        try{
            DefaultTableModel dtm=(DefaultTableModel)rTable.getModel();
            int rc=dtm.getRowCount();
            while(rc--!=0){
                dtm.removeRow(0);
            }
            ResultSet rs=db.DbConnect.st.executeQuery("select * from category order by category_name");
            int x=1;
            while(rs.next()){
                Vector row=new Vector();
                row.add(x++);
                row.add(rs.getString("category_name"));
                dtm.addRow(row);
            }
        }catch(Exception ex){
            JOptionPane.showMessageDialog(null, ex);
        }
    }
    
