    private void rTableMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_rTableMouseClicked
        // display row data to textfields
        int ri=rTable.getSelectedRow();
        if(ri!=-1){
            i.setText(String.valueOf((Integer)rTable.getValueAt(ri,0)));
            n.setText((String)rTable.getValueAt(ri,1));
            p.setText(String.valueOf((Integer)rTable.getValueAt(ri,2)));
            c.setSelectedItem((String)rTable.getValueAt(ri,3));
            h.setText(String.valueOf((Double)rTable.getValueAt(ri,4)));
            q.setText(String.valueOf((Integer)rTable.getValueAt(ri,5)));
        }
    }//GEN-LAST:event_rTableMouseClicked

    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // update product
        String name=n.getText();
        String pp=p.getText();
        String category=(String)c.getSelectedItem();
        String hh=h.getText();
        String qq=q.getText();
        if(name.equals("") || pp.equals("") || hh.equals("")|| qq.equals("")){
            JOptionPane.showMessageDialog(null,"Do not leave the field blank!");
        }else{
            try{
                int price=Integer.parseInt(pp);
                double height=Double.parseDouble(hh);
                int quantity=Integer.parseInt(qq);
                ResultSet rs=db.DbConnect.st.executeQuery("select * from products where name='"+name+"' and height="+height+"");
                if(rs.next()){
                    JOptionPane.showMessageDialog(null,"Product Alreday Exist!");
                }else{
                    int id=Integer.parseInt(i.getText());
                    db.DbConnect.st.executeUpdate("update products set name='"+name+"',price="+price+",category='"+category+"',height="+height+",quantity="+quantity+" where product_id="+id+"");
                    JOptionPane.showMessageDialog(null,"Success!");
                    displayTableData();
                }
            }catch(Exception ex){
                JOptionPane.showMessageDialog(null,ex);
            }
        }
    }//GEN-LAST:event_jButton1ActionPerformed

