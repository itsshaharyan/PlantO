    private void displayTableData(){
        try{
            String n=t.getText();
            DefaultTableModel dtm=(DefaultTableModel)rTable.getModel();
            int rc=dtm.getRowCount();
            while(rc--!=0){
                dtm.removeRow(0);
            }
            ResultSet rs=db.DbConnect.st.executeQuery("select * from products where name like '%"+n+"%' order by name");
            int x=1;
            while(rs.next()){
                Vector row=new Vector();
                row.add(rs.getInt("product_id"));
                row.add(rs.getString("name"));
                row.add(rs.getInt("price"));
                row.add(rs.getString("category"));
                row.add(rs.getDouble("height"));
                row.add(rs.getInt("quantity"));
                dtm.addRow(row);
            }
        }catch(Exception ex){
            JOptionPane.showMessageDialog(null, ex);
        }
    }
    
    
