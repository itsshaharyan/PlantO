    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        // delete product
        int result=JOptionPane.showConfirmDialog(null, "Do you really wanna delete", "Confirmation Dialog", JOptionPane.YES_NO_OPTION);
        if(result==JOptionPane.YES_OPTION){
            try{
                int id=Integer.parseInt(i.getText());
                db.DbConnect.st.executeUpdate("delete from products where product_id="+id+"");
                JOptionPane.showMessageDialog(null,"Success!");
                n.setText(null);
                p.setText(null);
                h.setText(null);
                q.setText(null);
                c.setSelectedIndex(0);
                i.setText("-----");
                displayTableData();
            }catch(Exception ex){
                JOptionPane.showMessageDialog(null,ex);
            }
        }
    }//GEN-LAST:event_jButton2ActionPerformed

